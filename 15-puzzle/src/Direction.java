
/**
 *Enum-luokka Direction määrittelee pelin tyhjän palan siirtosuunnan.
 */
public enum Direction {

    /**
     * Siirtosuunta vasemmalle
     */
    LEFT,
    /**
     * Siirtosuunta oikealle
     */
    RIGHT,
    /**
     * Siirtosuunta ylös
     */
    UP,
    /**
     * Siirtosuunta alas
     */
    DOWN;
}
